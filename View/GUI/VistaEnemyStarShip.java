    void setEnemy(EnemyToUI e){
        enemyDamagePanel.removeAll();
        damageView = new VistaDamage();
        damageView.setDamage(e.getDamage(), "PÃ©rdidas");
        enemyDamagePanel.add(damageView);
        
        enemyFire.setText(Float.toString(e.getAmmoPower()));
        
        enemyLootPanel.removeAll();
        lootView = new VistaLoot();
        lootView.setLoot(e.getLoot());
        enemyLootPanel.add(lootView);
        
        enemyName.setText(e.getName());
        enemyShield.setText(Float.toString(e.getShieldPower()));
        
        
        if(Controller.getInstance().getState() == GameState.BEFORECOMBAT || Controller.getInstance().getState() == GameState.INIT){
            setVisible(false);
        }else{
            setVisible(true);
        }
        
        repaint();
        revalidate();
    }

