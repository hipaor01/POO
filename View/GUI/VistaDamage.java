    void setDamage(DamageToUI damage, String title){
        if(damage == null){
            setVisible(false);
        }else{
            setBorder(BorderFactory.createTitledBorder(BorderFactory.createLineBorder(Color.BLACK),title));
            shields.setText(Integer.toString(damage.getNShields()));
            weapons.setText(damage.getWeaponInfo());
            repaint();
        }
    }

